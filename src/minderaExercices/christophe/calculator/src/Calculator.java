package minderaExercices.christophe.calculator.src;

public class Calculator {

    int sum(int num1, int num2){
        return num1 + num2;
    }

    int subtract(int num1, int num2){
        return num1 - num2;
    }

    int multiply(int num1, int num2){
        return num1 * num2;
    }

    int divide(int num1, int num2){
        return num1 / num2;
    }
}
